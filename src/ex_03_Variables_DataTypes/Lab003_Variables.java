package ex_03_Variables_DataTypes;

public class Lab003_Variables {
    public static void main(String[] args) {
        // syntax
        // data_type variable_name =  variable_value
        //```

        // Variable - Local Variable
        byte age_sakshi = 29;
        // byte -> data type - type of container
        //  age_sakshi ->  variable_name |  identifier
        // = -> Assignment Operator ?  (after some time)
        // 45 -> Variable Value |  Literal
        age_sakshi = 112;
        age_sakshi = 119;
        System.out.println(age_sakshi);
    }
}
